import { App } from "astal/gtk3"
import { Variable, GLib, bind } from "astal"
import { Astal, Gtk, Gdk } from "astal/gtk3"
import Hyprland from "gi://AstalHyprland"
import Mpris from "gi://AstalMpris"
import Battery from "gi://AstalBattery"
import Wp from "gi://AstalWp"
import Network from "gi://AstalNetwork"
import Tray from "gi://AstalTray"

function SysTray() {
    const tray = Tray.get_default()

    return <box className="SysTray">
        {bind(tray, "items").as(items => items.map(item => (
            <menubutton
                tooltipMarkup={bind(item, "tooltipMarkup")}
                usePopover={false}
                actionGroup={bind(item, "actionGroup").as(ag => ["dbusmenu", ag])}
                menuModel={bind(item, "menuModel")}>
                <icon gicon={bind(item, "gicon")} />
            </menubutton>
        )))}
    </box>
}

function Wifi() {
    const network = Network.get_default()
    const wifi = bind(network, "wifi")

    return <box visible={wifi.as(Boolean)}>
        {wifi.as(wifi => wifi && (
            <icon
                tooltipText={bind(wifi, "ssid").as(String)}
                className="Wifi"
                icon={bind(wifi, "iconName")}
            />
        ))}
    </box>
}

function AudioSlider() {
    const speaker = Wp.get_default()?.audio.defaultSpeaker!

    return <box className="AudioSlider" css="min-width: 140px">
        <icon icon={bind(speaker, "volumeIcon")} />
        <slider
            hexpand
            onDragged={({ value }) => speaker.volume = value}
            value={bind(speaker, "volume")}
        />
    </box>
}

function BatteryLevel() {
    const bat = Battery.get_default()

    return <box className="Battery"
        visible={bind(bat, "isPresent")}>
        <icon icon={bind(bat, "batteryIconName")} />
        <label label={bind(bat, "percentage").as(p =>
            `${Math.floor(p * 100)} %`
        )} />
    </box>
}

function Bluetooth() {
    const network = Network.get_default()
    const bluetooth = bind(network, "bluetooth")

    return <box visible={bluetooth.as(Boolean)}>
        {bluetooth.as(bluetooth => bluetooth && (
            <icon
                tooltipText={bind(bluetooth, "name").as(String)}
                className="Bluetooth"
                icon={bind(bluetooth, "iconName")}
            />
        ))}
    </box>
}

function SystemStats() {
    const ram = Variable<string>("").poll(1000, () =>
        GLib.spawn_command_line_sync("free -h | awk '/^Mem/ {print $3}'")[1]?.toString().trim() || "")
    const cpu = Variable<string>("").poll(1000, () =>
        GLib.spawn_command_line_sync("top -bn1 | grep 'Cpu(s)' | sed 's/.*, *\\([0-9.]*\\)%* id.*/\\1/' | awk '{print 100 - $1}'")[1]?.toString().trim() || "")
    const temp = Variable<string>("").poll(1000, () =>
        GLib.spawn_command_line_sync("sensors | awk '/^Package id 0:/ {print $4}'")[1]?.toString().trim() || "")

    return <box className="SystemStats">
        <label label={`RAM: ${ram()}`} />
        <label label={`CPU: ${cpu()}%`} />
        <label label={`Temp: ${temp()}`} />
    </box>
}

function Workspaces() {
    const hypr = Hyprland.get_default()

    const japaneseNumbers = ["一", "二", "三", "四", "五", "六", "七", "八", "九", "十"]

    return <box className="Workspaces">
        {bind(hypr, "workspaces").as(wss => wss
            .filter(ws => !(ws.id >= -99 && ws.id <= -2)) // filter out special workspaces
            .sort((a, b) => a.id - b.id)
            .map(ws => (
                <button
                    className={bind(hypr, "focusedWorkspace").as(fw =>
                        ws === fw ? "focused" : "")}
                    onClicked={() => ws.focus()}>
                    {ws.id >= 1 && ws.id <= 10 ? japaneseNumbers[ws.id - 1] : ws.id}
                </button>
            ))
        )}
    </box>
}

function Time({ format = "%H:%M - %A %e." }) {
    const time = Variable<string>("").poll(1000, () =>
        GLib.DateTime.new_now_local().format(format)!)

    return <label
        className="Time"
        onDestroy={() => time.drop()}
        label={time()}
    />
}

export default function Bar(monitor: Gdk.Monitor) {
    const { TOP, LEFT, RIGHT } = Astal.WindowAnchor

    return <window
        className="Bar"
        gdkmonitor={monitor}
        exclusivity={Astal.Exclusivity.EXCLUSIVE}
        anchor={TOP | LEFT | RIGHT}>
        <centerbox>
            <box hexpand halign={Gtk.Align.START}>
                <SystemStats />
            </box>
            <box>
                <Workspaces />
            </box>
            <box hexpand halign={Gtk.Align.END} >
                <SysTray />
                <Wifi />
                <Bluetooth />
                <AudioSlider />
                <BatteryLevel />
                <Time />
            </box>
        </centerbox>
    </window>
}
